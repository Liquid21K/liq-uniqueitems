local QBCore = exports['qb-core']:GetCoreObject()


RegisterNetEvent("liquid-unique:server:updateclothing")
AddEventHandler("liquid-unique:server:updateclothing", function(categories, number)
    local src = source
    local Player = QBCore.Functions.GetPlayer(src)
    Player.Functions.ChangeUniqueClothing(categories, number)
    print(number)
    
end)

function RandomItem()
    return Config.RandomItemDrop[math.random(#Config.RandomItemDrop)]
end


RegisterNetEvent("liquid:server:randomitem")
AddEventHandler("liquid:server:randomitem", function()
    local src = source
    local Player = QBCore.Functions.GetPlayer(src)
    Player.Functions.AddItem(RandomItem(), 1)
    
end)


QBCore.Commands.Add('removeclothing', "remove player clothing access", { { name = "id", help = "player id" }, { name = "category", help = "pants/chain/mask/hat/torso/tshirt/bag/shoes" }, { name = "number", help = "the clothing number" } }, true, function(source, args)
    local Player = QBCore.Functions.GetPlayer(tonumber(args[1]))
    local Category = tostring(args[2]):lower()
    local number = tonumber(args[3])
    if Player and Category ~= nil and number ~= nil then
        Player.Functions.RemoveUniqueClothing(Category, number)
    end
end, 'god')


QBCore.Commands.Add('addclothing', "add player clothing", { { name = "id", help = "player id" }, { name = "category", help = "pants/chain/mask/hat/torso/tshirt/bag/shoes" }, { name = "number", help = "the clothing number" } }, true, function(source, args)
    local Player = QBCore.Functions.GetPlayer(tonumber(args[1]))
    local Category = tostring(args[2]):lower()
    local number = tonumber(args[3])
    if Player and Category ~= nil and number ~= nil then
        Player.Functions.ChangeUniqueClothing(Category, number)
    end
end, 'god')

